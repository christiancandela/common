package org.dbdoclet.jive.widget.imagemap;

public class ImageMapException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public ImageMapException() {
        super();
    }

    public ImageMapException(String message) {
        super(message);
    }

    public ImageMapException(Throwable cause) {
        super(cause);
    }

    public ImageMapException(String message, Throwable cause) {
        super(message, cause);
    }

}
